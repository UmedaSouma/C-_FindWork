//========================================================================================================================
// 描画処理
//========================================================================================================================
void CBillboard::Draw()
{
	CObject3D::InitMatrix();

	CObject3D::ViewMatrix();

	CObject3D::YawPitchRoll();
	CObject3D::SetMatrix();


	CObject3D::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CBillboard* CBillboard::Create(D3DXVECTOR3 pos)
{
	CBillboard* pBillboard = new CBillboard;
	pBillboard->SetPos(pos);

	pBillboard->Init();

	return pBillboard;
}