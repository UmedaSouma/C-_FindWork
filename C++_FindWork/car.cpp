//===========================================================================================================
// アクセル処理
//===========================================================================================================
void CCar::ActionAccele()
{
	D3DXVECTOR3 move = GetMove();

	if (move.z < 150.0f)
	{
		move.x += sinf(/*pCamera->GetRot().y+*/ GetRot().y)/*+ sinf()*/ * 0.5f;
		move.z += cosf(/*pCamera->GetRot().y+*/ GetRot().y)/*+ cosf()*/ * 0.5f;
		//move.z += 0.5f;
	}

	SetMove({ move.x,0.0f,move.z });
}

//===========================================================================================================
// ブレーキ処理
//===========================================================================================================
void CCar::ActionBrake()
{
	D3DXVECTOR3 move = GetMove();

	move.z -= 0.1f;

	SetMove({ 0.0f,0.0f,move.z });
}

//===========================================================================================================
// カーブ処理
//===========================================================================================================
void CCar::ActionBend_R()
{
	D3DXVECTOR3 rot = GetRot();
	
	rot.y += 0.05f;
	SetRot(rot);
}

//===========================================================================================================
// カーブ処理
//===========================================================================================================
void CCar::ActionBend_L()
{
	D3DXVECTOR3 rot = GetRot();

	rot.y += -0.05f;
	SetRot(rot);
}

