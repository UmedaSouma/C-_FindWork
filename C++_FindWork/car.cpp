#include "car.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CCar::CCar()
{
	// パラメーターの設定

	m_Param.fWeight = MAX_WEIGHT;
	m_Param.nGearNum = MAX_GEAR;
	m_Param.fBending = MAX_BENDING;
	m_Param.nMaxLife = MAX_LIFE;
	m_Param.nLife = m_Param.nMaxLife;
	
	for (int i = 0; i < m_Param.nGearNum; i++)
	{
		m_Param.fMaxSpeed[i] = MAX_SPEED[i];
	}

	m_type = CAR_NORMAL;
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CCar::~CCar()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CCar::Init()
{
	CModel::Init();

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CCar::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CCar::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CCar::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CCar* CCar::Create()
{
	CCar* pCar = new CCar;

	pCar->Init();

	return pCar;
}