//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CCar::CCar()
{
	// パラメーターの設定

	m_Param.fWeight = MAX_WEIGHT;
	m_Param.nMaxGear = MAX_GEAR;
	m_Param.fBending = MAX_BENDING;
	m_Param.nMaxLife = MAX_LIFE;
	
	for (int i = 0; i < m_Param.nMaxGear; i++)
	{
		m_Param.fMaxSpeed[i] = MAX_SPEED[i];
	}

	m_type = CParamStorage::CAR_NORMAL;
}

