#include "car_enemy.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CCarEnemy::CCarEnemy()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CCarEnemy::~CCarEnemy()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CCarEnemy::Init()
{
	SetType(TYPE::CAR_ENEMY);

	CCar::Init();

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CCarEnemy::Uninit()
{
	CCar::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CCarEnemy::Update()
{
	CCar::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CCarEnemy::Draw()
{
	CCar::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CCarEnemy* CCarEnemy::Create(CParamStorage::TYPE cartype, D3DXVECTOR3 pos)
{
	CCarEnemy* pCarEnemy = new CCarEnemy;

	pCarEnemy->SetPos(pos);
	pCarEnemy->SetTypeCar(cartype);

	pCarEnemy->Init();

	return pCarEnemy;
}